# 7:有1 2 3 4 这四个数字，能组成多少个互不相同且无重复数字的三位数？分别是什么？
#123 123 123  111 222 121 131 122
count=0#记录符合条件的数据个数
L=[]#存储符合条件的的数字
for i in range(1,5):#个位数
    for j in range(1,5):#j 十位数
        for k in range(1,5):#百位数
            if i!=j and j!=k and i!=k:
                a=k*100+j*10+i
                count+=1
                L.append(a)
print('满足条件的数字个数是：',count)
print('满足条件的数字分别是：',L)
